package com.cross.model;

public class Notice {
	public int n_no;
	public String user_id;
	public int n_chk;
	public String n_subject;
	public String n_content;
	public String file;
	public int n_views;
	public String n_wdate;
	public String n_mdate;
	
	public Notice(){
	}

	public int getN_no() {
		return n_no;
	}

	public void setN_no(int n_no) {
		this.n_no = n_no;
	}

	public String getUser_id() {
		return user_id;
	}

	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

	public int getN_chk() {
		return n_chk;
	}

	public void setN_chk(int n_chk) {
		this.n_chk = n_chk;
	}

	public String getN_subject() {
		return n_subject;
	}

	public void setN_subject(String n_subject) {
		this.n_subject = n_subject;
	}

	public String getN_content() {
		return n_content;
	}

	public void setN_content(String n_content) {
		this.n_content = n_content;
	}

	public String getFile() {
		return file;
	}

	public void setFile(String file) {
		this.file = file;
	}

	public int getN_views() {
		return n_views;
	}

	public void setN_views(int n_views) {
		this.n_views = n_views;
	}

	public String getN_wdate() {
		return n_wdate;
	}

	public void setN_wdate(String n_wdate) {
		this.n_wdate = n_wdate;
	}

	public String getN_mdate() {
		return n_mdate;
	}

	public void setN_mdate(String n_mdate) {
		this.n_mdate = n_mdate;
	}
	
	
}
